#ifndef _CARD_TYPES_H_
#define _CARD_TYPES_H_

/**
 * @brief 卡牌花色类型枚举
 * 定义标准纸牌的花色类型
 */
enum CardSuitType
{
    CST_NONE = -1,
    CST_CLUBS,      // 梅花
    CST_DIAMONDS,   // 方块
    CST_HEARTS,     // 红桃
    CST_SPADES,     // 黑桃
    CST_NUM_CARD_SUIT_TYPES
};

/**
 * @brief 卡牌正面类型枚举
 * 定义标准纸牌的牌面类型（A, 2-10, J, Q, K）
 */
enum CardFaceType
{
    CFT_NONE = -1,
    CFT_ACE,
    CFT_TWO,
    CFT_THREE,
    CFT_FOUR,
    CFT_FIVE,
    CFT_SIX,
    CFT_SEVEN,
    CFT_EIGHT,
    CFT_NINE,
    CFT_TEN,
    CFT_JACK,
    CFT_QUEEN,
    CFT_KING,
    CFT_NUM_CARD_FACE_TYPES
};

#endif // _CARD_TYPES_H_
